    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String nama = txtIdPasien.getText();
        int umur = Integer.parseInt(txtUmur.getText());
        String jenisKelamin = (String) cmbJenisKelamin.getSelectedItem();
        String alamat = txtAlamat.getText();
        
        if (nama.isEmpty() || alamat.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama dan alamat harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            Pasien pasienBaru = new Pasien(nama, umur, jenisKelamin, alamat);
            try {
                pasienBaru.tambahPasien();
                JOptionPane.showMessageDialog(this, "Data pasien berhasil disimpan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                // Setelah menyimpan, kosongkan field
                txtNama.setText("");
                txtUmur.setText("");
                txtAlamat.setText("");
                
                tampilkanDataPasien();
                
            } catch (SQLException ex) {
                Logger.getLogger(FramePasien.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Gagal menyimpan data pasien!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        int idPasien = Integer.parseInt(txtIdPasien.getText());
        String nama = txtNama.getText();
        int umur = Integer.parseInt(txtUmur.getText());
        String jenisKelamin = (String) cmbJenisKelamin.getSelectedItem();
        String alamat = txtAlamat.getText();

        if (nama.isEmpty() || alamat.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama dan alamat harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            Pasien pasien = new Pasien();
            pasien.setIdPasien(idPasien);
            pasien.setNama(nama);
            pasien.setUmur(umur);
            pasien.setJenisKelamin(jenisKelamin);
            pasien.setAlamat(alamat);
            try {
                pasien.updatePasien();
                JOptionPane.showMessageDialog(this, "Data pasien berhasil diperbarui!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                // Refresh tabel jika diperlukan
                // refreshTabelPasien();
                
                tampilkanDataPasien();
            } catch (SQLException ex) {
                Logger.getLogger(FramePasien.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Gagal memperbarui data pasien!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
       int idPasien = Integer.parseInt(txtIdPasien.getText());
        
        int confirm = JOptionPane.showConfirmDialog(this, "Anda yakin ingin menghapus data pasien?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                Pasien.hapusPasien(idPasien);
                JOptionPane.showMessageDialog(this, "Data pasien berhasil dihapus!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                // Refresh tabel jika diperlukan
                // refreshTabelPasien();
                
                tampilkanDataPasien();
            } catch (SQLException ex) {
                Logger.getLogger(FramePasien.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Gagal menghapus data pasien!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

