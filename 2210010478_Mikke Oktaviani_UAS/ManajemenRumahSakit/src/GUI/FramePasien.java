    private void tampilkanDataPasien() {
                try {
            List<Pasien> daftarPasien = Pasien.semuaPasien();
            DefaultTableModel model = (DefaultTableModel) pasienTable.getModel();
            model.setRowCount(0); // Menghapus semua baris yang ada saat ini
            
            for (Pasien pasien : daftarPasien) {
                Object[] row = {pasien.getIdPasien(), pasien.getNama(), pasien.getUmur(), pasien.getJenisKelamin(), pasien.getAlamat()};
                model.addRow(row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FramePasien.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Gagal mengambil data pasien!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
